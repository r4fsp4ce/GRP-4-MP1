import math

a = float(input("Enter value of a: "))
b = float(input("Enter value of b: "))

print(f"Sum : {a + b}")
print(f"Difference : {a - b}")
print(f"Product: {a * b}")
print(f"Quotient: {a / b}")
print(f"Remainder: {a % b}")
print(f"Log: {math.log10(a)}")
print(f"Exponent: {a ** b}")
